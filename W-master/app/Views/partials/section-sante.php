                    <p>
                       

                         « En 2014, le virus Ebola frappait l’Afrique de l’Ouest, constituant une catastrophe sanitaire, humanitaire et économique d’une ampleur inégalée, marquée par un bilan humain extrêmement lourd.

                        Dès le début de la crise, la Croix-Rouge française a répondu présente. Elle s’est engagée sur plusieurs terrains afin de participer activement à la lutte contre l’épidémie, en Afrique mais également en France. »</p>

                        <p>Très rapidement, le ministère des Affaires étrangères et du Développement international (MAEDI) nous a demandé d’être l’opérateur du Gouvernement français. En coordination avec l’ensemble des acteurs impliqués (ministère des Affaires étrangères, Etablissement public de réponse aux urgences sanitaires (EPRUS), ministère de la Santé, Médecins sans frontières…), nous avons déployé nos actions en Guinée. Sur place, nous avons assumé la gestion de Centres de Traitement Ebola (CTE), principalement à Macenta et à Forécariah pour soutenir et renforcer les capacités de réponse de la Croix-Rouge guinéenne face à l’épidémie. 

                        Parallèlement, et ce fut une grande première en France, la Croix-Rouge française a mis en place dans ses locaux la première formation Ebola francophone, afin d’assurer l’indispensable préparation au départ en mission de ses volontaires, des volontaires EPRUS et des ONG engagées dans la lutte contre l’épidémie. 

                        Nos bénévoles ont également assuré pendant de longues semaines des dispositifs de prévention et de prise en charge à l’aéroport de Roissy pour les voyageurs arrivant d’Afrique de l’Ouest.</p>
                    
